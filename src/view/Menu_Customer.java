    private void btn_addToCartActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_addToCartActionPerformed
        if (isLogin) { // Kiểm tra xem người dùng đã đăng nhập chưa
        // Lấy thông tin sản phẩm từ bảng
            int selectedRow = table_showProduct.getSelectedRow(); // Giả sử bạn có bảng sản phẩm có tên là productTable
            if (selectedRow != -1) { 
                try {
                    // Nếu có chọn sản phẩm trong bảng
                    String productId = table_showProduct.getValueAt(selectedRow, 0).toString(); // Giả sử cột 0 là mã sản phẩm
                    String productName = table_showProduct.getValueAt(selectedRow, 1).toString(); // Cột 1 là tên sản phẩm
                    double price = Double.parseDouble(table_showProduct.getValueAt(selectedRow, 3).toString()); // Cột 2 là giá sản phẩm
//                    int quantity = 1; // Mặc định số lượng là 1 (có thể thay đổi nếu có input từ người dùng)
                    int quantityToAdd = (int) txt_quatitytoadd.getValue();
                    if(quantityToAdd<1){
                        quantityToAdd = 1;
                    }
                    // Kiểm tra xem người dùng đã có giỏ hàng chưa
                    CartDAO cartDAO = new CartDAO();
                    Cart cart = cartDAO.getCartByUserId(userId); // Lấy giỏ hàng của người dùng

                    if (cart == null) {
                        // Nếu người dùng chưa có giỏ hàng, tạo giỏ hàng mới
                        cart = new Cart("cart" + userId, userId, 0);
                        if (!cartDAO.addCart(cart)) {
                            JOptionPane.showMessageDialog(this, "Không thể tạo giỏ hàng mới", "Lỗi", JOptionPane.ERROR_MESSAGE);
                            return;
                        }
                    }
                    // Thêm sản phẩm vào giỏ hàng
                    boolean success = cartDAO.addProductToCart(cart.getCartId(), productId, quantityToAdd);
                    if (success) {
                        // Cập nhật lại tổng giá trị giỏ hàng
                        double totalPrice = cartDAO.calculateTotalPrice(cart.getCartId());
                        cartDAO.updateTotalPrice(cart.getCartId(), totalPrice);
                        JOptionPane.showMessageDialog(this, "Sản phẩm đã được thêm vào giỏ hàng", "Thành công", JOptionPane.INFORMATION_MESSAGE);
                    } else {
                        JOptionPane.showMessageDialog(this, "Không thể thêm sản phẩm vào giỏ hàng", "Lỗi", JOptionPane.ERROR_MESSAGE);
                    }
                } catch (SQLException ex) {
                    Logger.getLogger(Menu_Customer.class.getName()).log(Level.SEVERE, null, ex);
                }
            } else {
                JOptionPane.showMessageDialog(this, "Vui lòng chọn sản phẩm để thêm vào giỏ hàng", "Chưa chọn sản phẩm", JOptionPane.WARNING_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Vui lòng đăng nhập để thêm vào giỏ hàng.", "Đăng nhập yêu cầu", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btn_addToCartActionPerformed

    private void jLb_searchMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLb_searchMouseClicked
        try {
            String search = txt_search.getText().trim();
            ProductDAO productDAO = new ProductDAO();
            String[][] products;

            if (search.isEmpty()) {
                // Nếu không có từ khóa, hiển thị toàn bộ dữ liệu
                products = productDAO.readProductsTableData();
            } else {
                // Nếu có từ khóa, thực hiện tìm kiếm
                products = productDAO.searchProducts(search);
            }
            DefaultTableModel model = new DefaultTableModel(products,
                new String[]{"ProductId", "Name", "Description", "Price", "Quantity In Stock", "Category", "Brand", "Status"}) {
                @Override
                public boolean isCellEditable(int row, int column) {
                    return false; // Không cho phép chỉnh sửa bất kỳ ô nào
                }
            };
            table_showProduct.setModel(model);
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jLb_searchMouseClicked

